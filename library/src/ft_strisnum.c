#include "libft.h"

int	ft_strisnum(const char *str)
{
	int	i;

	i = 0;
	if (str[0] == '=')
		i++;
	if (str[i] == '+' || str[i] == '-')
		i++;
	if (!str[i])
		return (0);
	while (str[i])
	{
		if (str[i] < '0' || str[i] > '9')
			return (0);
		i++;
	}
	return (1);
}
